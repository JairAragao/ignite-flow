let currentLang = 'pt'

function setLang(lang) {
  currentLang = lang
}

function getLang() {
  return currentLang
}

function t(key, params = {}) {
  const keys = key.split('.')
  let value = messages[currentLang]
  for (const k of keys) {
    value = value?.[k]
  }
  if (!value) return key

  return value.replace(/\{(\w+)\}/g, (_, p) => params[p] ?? `{${p}}`)
}

module.exports = {
  setLang,
  getLang,
  t,
  messages,
}
